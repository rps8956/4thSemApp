my_list = [1,5,"apple",3.4,7]
#add new elements to the end of the list
my_list.append("grapes")
print("Appended list:",my_list)

#reverse the elements of the list
my_list.reverse()
print("Reversed list:",my_list)

#display same list element multiple times
print("Displaying elements multiple times",my_list*3)

#To concatenate 2 lists
my_list2 = [45,67,12,9,99]
print("Concatenated list is:",my_list+my_list2)

#Sorting list in ascending order
my_list2.sort()
print("Sorted list:",my_list2)